package com.invoice.purchase.beans;

public class VenderDetails {

	private int txtSrNo;
	private String txtName;
	private String txtPersonName;
	private String txtCon;
	private String txtMail;
	private String txtDes;
	public int getTxtSrNo() {
		return txtSrNo;
	}
	public void setTxtSrNo(int txtSrNo) {
		this.txtSrNo = txtSrNo;
	}
	public String getTxtName() {
		return txtName;
	}
	public void setTxtName(String txtName) {
		this.txtName = txtName;
	}
	public String getTxtPersonName() {
		return txtPersonName;
	}
	public void setTxtPersonName(String txtPersonName) {
		this.txtPersonName = txtPersonName;
	}
	
	public String getTxtCon() {
		return txtCon;
	}
	public void setTxtCon(String txtCon) {
		this.txtCon = txtCon;
	}
	public String getTxtMail() {
		return txtMail;
	}
	public void setTxtMail(String txtMail) {
		this.txtMail = txtMail;
	}
	public String getTxtDes() {
		return txtDes;
	}
	public void setTxtDes(String txtDes) {
		this.txtDes = txtDes;
	}
	
	
}
